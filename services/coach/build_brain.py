# ---------------------------------------------------------------------------
# 1. Resolve the manifest → concrete source files (fail-closed)
# ---------------------------------------------------------------------------


def resolve_sources(manifest: list[str], root: Path) -> list[Path]:
    """Expand each manifest entry to one-or-more *.md files under `root`.

    Fail-closed: an entry resolving to ZERO files raises — a moved/renamed source
    blocks the build instead of silently dropping content from the brain.
    """
    files: list[Path] = []
    for entry in manifest:
        target = (root / entry).expanduser()
        if target.is_dir():
            found = sorted(target.rglob("*.md"))
            if not found:
                raise SystemExit(f"FAIL-CLOSED: manifest dir '{entry}' has no *.md under {root}")
            files.extend(found)
        elif target.is_file():
            files.append(target)
        else:
            raise SystemExit(f"FAIL-CLOSED: manifest entry '{entry}' not found under {root}")
    # de-dup, preserve order
    seen: set[Path] = set()
    unique = [f for f in files if not (f in seen or seen.add(f))]
    return unique


# ---------------------------------------------------------------------------
# 2. Assemble one big document with provenance headers
# ---------------------------------------------------------------------------


def assemble(files: list[Path], root: Path) -> str:
    stamp = datetime.now(timezone.utc).isoformat()
    parts: list[str] = [
        f"<!-- GENERATED by build_brain.py at {stamp} — DO NOT HAND-EDIT. "
        f"Edit the MANIFEST and re-run. {len(files)} source docs. -->\n"
    ]
    for f in files:
        rel = f.relative_to(root)
        parts.append(f"\n\n---\n\n<!-- SOURCE: {rel} -->\n# {rel}\n\n{f.read_text(encoding='utf-8')}")
    return "".join(parts)


def write_big_doc(text: str, out: Path) -> None:
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(text, encoding="utf-8")


