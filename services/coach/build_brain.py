def main() -> int:
    ap = argparse.ArgumentParser(description="Build the coach value-prop + stakeholder corpus from the THJ doc manifest.")
    ap.add_argument("--dry-run", action="store_true", help="assemble + write the big doc; skip ingest")
    ap.add_argument("--source-root", type=Path, default=SOURCE_ROOT, help=f"docs root (default {SOURCE_ROOT})")
    ap.add_argument("--output", type=Path, default=OUTPUT_PATH, help=f"assembled big-doc path (default {OUTPUT_PATH})")
    args = ap.parse_args()

    if not MANIFEST or all(not e.strip() for e in MANIFEST):
        print("MANIFEST is empty — nothing to build. Author the THJ doc paths in build_brain.py, then re-run.", file=sys.stderr)
        return 2

    if not args.source_root.is_dir():
        raise SystemExit(f"FAIL-CLOSED: source root not found: {args.source_root}")

    files = resolve_sources(MANIFEST, args.source_root)
    print(f"[build_brain] resolved {len(files)} source docs from {len(MANIFEST)} manifest entries")

    big_doc = assemble(files, args.source_root)
    write_big_doc(big_doc, args.output)
    print(f"[build_brain] wrote assembled corpus → {args.output} ({len(big_doc):,} chars)")

    if args.dry_run:
        print("[build_brain] --dry-run: skipping ingest")
        return 0

    ingest(big_doc)
    print(f"[build_brain] ingested → corpus '{CORPUS_TITLE}'")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
