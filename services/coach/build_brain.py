"""build_brain.py — re-runnable, manifest-driven build of the coach's value-prop
+ stakeholder corpus.

Pattern (from the THJ TKR_GENERIC derivation, thj plan 26-6-18): author intent on
the SOURCE — here, the MANIFEST below — never hand-edit the target. Re-run anytime
to track the latest source docs with zero drift. The corpus is *generated*; a
hand-maintained corpus rots the moment a source doc improves.

Pathways
--------
  INPUT      ~/repo_docs/thj/<manifest path>            (override: COACH_BRAIN_SOURCE_ROOT)
             — every MANIFEST entry is a path under this root. A directory entry
               pulls every *.md beneath it (recursive); a file entry pulls just it.
  ASSEMBLED  services/coach/brain/coach_brain_corpus.md  (inspectable, gitignored)
             — all sources concatenated into ONE big document with provenance
               headers, the artifact a human can eyeball before it ships.
  CORPUS     kb Postgres reference_docs + reference_doc_chunks                (ingest)
             — one logical doc "THJ Coach Brain — value-prop + stakeholders",
               chunked + embedded (shared-svcs), upserted idempotently so a
               re-run replaces cleanly.

Fail-closed: a MANIFEST entry that does not resolve to at least one file HARD-FAILS
the build (non-zero exit, names the entry) — never ships a partial brain. An empty
MANIFEST exits 2 with guidance rather than silently building nothing.

Usage
-----
  uv run python services/coach/build_brain.py              # assemble + ingest
  uv run python services/coach/build_brain.py --dry-run    # assemble + write big doc only
"""
